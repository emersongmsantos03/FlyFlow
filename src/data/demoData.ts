const createChecklist = (projectId: string): ProjectChecklistItem[] => {
  const buildItems = (
    items: string[],
    category: ProjectChecklistItem['category'],
    offset: number,
  ) =>
    items.map((title, index) => ({
      id: `chk-${projectId}-${offset + index}`,
      projectId,
      title,
      category,
      completed: index < 2 && category === 'Antes da captação',
      completedAt: index < 2 && category === 'Antes da captação' ? day(-1) : undefined,
      orderIndex: offset + index,
      createdAt: day(-10),
    }))

  return [
    ...buildItems(beforeCaptureChecklist, 'Antes da captação', 1),
    ...buildItems(duringCaptureChecklist, 'Durante a captação', 50),
    ...buildItems(afterCaptureChecklist, 'Depois da captação', 100),
  ]
}

